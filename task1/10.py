import math

print("Enter two integers: ")
a = int(input())
b = int(input())
print("Sum: " + str(a + b))
print("Difference: " + str(a - b))
print("Product: " + str(a * b))
print("Quotient: " + str(a / b))
print("Remainder: " + str(a % b))
print("Log10 (A): " + str(math.log(a, 10)))
print("Result of AB: " + str(a) + str(b))